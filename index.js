/* BURGER MENU */

function openMenu() {
    document.body.classList += " menu--open"
}

function closeMenu() {
    document.body.classList.remove('menu--open')
}

/* FAQ TABS */

function openTab() {
    document.body.classList += "tab--open"
}

function closeTab() {
    document.body.classList.remove('tab--open')
}
